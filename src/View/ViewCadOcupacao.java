    void carregarCargos() {

        if ((int) tbUsuarios.getRowCount() > 0) {
            DefaultTableModel modelo = (DefaultTableModel) tbUsuarios.getModel();
            modelo.setNumRows(0);
        }
        try {
            DefaultTableModel model = (DefaultTableModel) tbUsuarios.getModel();

            int codigo = 0;
            if (!"Novo".equals(tfCodigo.getText())) {
                codigo = (Integer.parseInt(tfCodigo.getText()));
            }

            String consultar = "SELECT * FROM ocupacao";
            String[] datos = new String[3];
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(consultar);
            while (rs.next()) {
                datos[0] = rs.getString("codigo");
                int codOcupacao = (Integer.parseInt(rs.getString("cod_permissao")));
                String NomePermissao = (String.valueOf(cbPermissao.getItemAt(codOcupacao)));
                datos[1] = NomePermissao;
                datos[2] = rs.getString("ocup_descricao");

                model.addRow(datos);
            }
            tbUsuarios.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(ViewCadOcupacao.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

