    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        if (cbPermissao.getSelectedIndex() <= 0) {
            jLcbPermissaoAlerta.setVisible(true);
        } else {
            jLcbPermissaoAlerta.setVisible(false);
        }
        if ("".equals(tfNome.getText())) {
            jlCampoNomeAlerta.setVisible(true);
        } else {
            jlCampoNomeAlerta.setVisible(false);
        }

        if (cbPermissao.getSelectedIndex() > 0 && !("".equals(tfNome.getText()))) {
            String insertar = "INSERT INTO ocupacao(cod_permissao,ocup_descricao) VALUES (?,?)";
            try {
                // variável para obter o número de bytes de uma imagem.
                PreparedStatement pst = cn.prepareStatement(insertar);
                pst.setInt(1, cbPermissao.getSelectedIndex());
                pst.setString(2, tfNome.getText());

                int i = pst.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "OCUPAÇÃO SALVO COM SUCESSO!");
                }
                bloquearBotoes();
                //numeros();
                carregarCargos();
                limparCampos();
            } catch (SQLException ex) {
                Logger.getLogger(ViewCadOcupacao.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btSalvarActionPerformed

