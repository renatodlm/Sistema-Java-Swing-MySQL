    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        if (tfNome.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencha o campo nome", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        } else {

            String insertar = "INSERT INTO cobertura(cober_nome) VALUES (?)";
            try {
                // variável para obter o número de bytes de uma imagem.
                PreparedStatement pst = cn.prepareStatement(insertar);
                pst.setString(1, tfNome.getText());

                int i = pst.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "Tipo de Conta adicionada com sucesso!");
                }
                carregarConexoes();
                limparCamposBanco();
            } catch (SQLException ex) {
                Logger.getLogger(ViewCadTipoContas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        int fila = tbTipoConta.getSelectedRow();
        if (fila > -1) {
            String cod = tbTipoConta.getValueAt(fila, 0).toString();
            String sqlElim = "DELETE FROM cobertura WHERE codigo='" + cod + "'";
            try {
                PreparedStatement pst = cn.prepareStatement(sqlElim);
                int n = pst.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "Tipo de Conta apagado com sucesso!");
                    carregarConexoes();

                } else {
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro ao apagar Tipo de Conta");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ViewCadTipoContas.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        int fila = tbTipoConta.getSelectedRow();
        if (fila >= 0) {
            btSalvar.setEnabled(false);
            btAtualizar.setEnabled(true);
            btCancelar.setEnabled(true);
            String cod = tbTipoConta.getValueAt(fila, 0).toString();
            String ocupacao = tbTipoConta.getValueAt(fila, 1).toString();
            tfCodigo.setText(cod);
            tfNome.setText(ocupacao);
        } else {
            JOptionPane.showMessageDialog(null, "Escolha uma linha na tabela antes!");
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        // TODO add your handling code here:
        int cod = 0;
        cod = (Integer.parseInt(tfCodigo.getText()));
        try {
            String sql = "UPDATE cobertura SET codigo = ?, "
                    + "cober_nome = ?"
                    + "WHERE codigo = '" + tfCodigo.getText() + "'";

            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, tfCodigo.getText());
            pst.setString(2, tfNome.getText());

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Tipo de Conta atualizado com sucesso!");
                btSalvar.setEnabled(true);
                btAtualizar.setEnabled(false);
                carregarConexoes();
                cancelar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao atualizar Tipo de Conta");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewCadTipoContas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAtualizarActionPerformed

    private void jbExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExportarActionPerformed

        if (tbTipoConta.getRowCount() > 0) {
            JFileChooser chooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de excel", "xls");
            chooser.setFileFilter(filter);
            chooser.setDialogTitle("Gaurdar Arquivo");
            chooser.setAcceptAllFileFilterUsed(false);
            if (chooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                List<JTable> tb = new ArrayList<JTable>();
                List<String> nom = new ArrayList<String>();
                tb.add(tbTipoConta);
                nom.add("Pessoas");
                String file = chooser.getSelectedFile().toString().concat(".xls");
                try {
                    Exporter e = new Exporter(new File(file), tb, nom);
                    if (e.export()) {
                        JOptionPane.showMessageDialog(null, "Os dados foram exportados com sucesso!", "ARQUIVO EXCEL", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro Exception" + e.getMessage(), " Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Não há dados para exportar na tabela", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExportarActionPerformed

