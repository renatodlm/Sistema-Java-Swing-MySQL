    void carregarConexoes() {
        if ((int) tbTipoConta.getRowCount() > 0) {
            DefaultTableModel modelo = (DefaultTableModel) tbTipoConta.getModel();
            modelo.setNumRows(0);
        }
        try {
            DefaultTableModel model = (DefaultTableModel) tbTipoConta.getModel();
            int codigo = 0;
            if (!"Novo".equals(tfCodigo.getText())) {
                codigo = (Integer.parseInt(tfCodigo.getText()));
            }

            String consultar = "SELECT * FROM cobertura";
            String[] datos = new String[5];
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(consultar);
            while (rs.next()) {
                datos[0] = rs.getString("codigo");
                datos[1] = rs.getString("cober_nome");

                model.addRow(datos);
            }
            tbTipoConta.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(ViewCadTipoContas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

